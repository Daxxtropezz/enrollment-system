    private void schedTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_schedTblMouseClicked
        RecordTable = (DefaultTableModel) schedTbl.getModel();
        int SelectedRows = schedTbl.getSelectedRow();

        subjectDetailsForm sdf = new subjectDetailsForm();

        String subject = (RecordTable.getValueAt(SelectedRows, 1).toString());

        try {
            conn = DriverManager.getConnection(URL, USERNAME, PASSWORD);
            ps = conn.prepareStatement("SELECT * FROM schedule WHERE s_subject = ?");

            ps.setString(1, subject);

            rset = ps.executeQuery();

            if (rset.next()) {
                String teacher = rset.getString("s_teacher");
                String day = rset.getString("s_day");
                String start = rset.getString("s_starttime");
                String end = rset.getString("s_endtime");
                String description = rset.getString("s_description");

                subjectDetailsForm.subNameDET.setText(subject);
                subjectDetailsForm.teachDET.setText(teacher);
                subjectDetailsForm.dayDET.setText(day);
                subjectDetailsForm.startDET.setText(start);
                subjectDetailsForm.endDET.setText(end);
                subjectDetailsForm.descDET.setText(description);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        sdf.setVisible(true);
        sdf.setLocationRelativeTo(null);
        sdf.pack();
    }//GEN-LAST:event_schedTblMouseClicked

